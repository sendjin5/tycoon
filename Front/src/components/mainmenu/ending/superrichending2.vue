  <div class="main-container">
    <div class="dialog-box">
      <div class="dialog-text" v-html="inputText"></div>
      <div class="dialog-arrow" @click="movePage">▼</div>
    </div>
  </div>
</template>

<script>
export default {
  name: 'ending4',
  data() {
    return {
      inputText: ''
    };
  },
  methods:{
    movePage(){
      this.$router.push('/superrichending3')
    }
  },
  mounted() {
    let innerText = '내 노력과 열정이 전국을 넘어 세계로 퍼졌고 하루 매출이 남들의 연봉을 뛰어넘은 지도 오래야\n 이제 나는 트렌드를 만들고 세상의 흐름은 내 손 끝에서 시작돼';

    for (let i = 0; i < innerText.length; i++) {
      setTimeout(() => {
        if (innerText[i] === '\n') {
          this.inputText += '<br>';
        } else {
          this.inputText += innerText[i];
        }
      }, i * 50);
    }
  }
};
</script>
