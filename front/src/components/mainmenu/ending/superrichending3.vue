<template>
  <div class="main-container">
    <div class="dialog-box">
      <div class="dialog-text" v-html="inputText"></div>
      <div class="dialog-arrow" @click="movePage">▼</div>
    </div>
  </div>
</template>

<script>
export default {
  name: 'ending4',
  data() {
    return {
      inputText: ''
    };
  },
  methods:{
    movePage(){
      this.$router.push('/homeMenu')
    }
  },
  mounted() {
    let innerText = '하지만 이 모든 것은 우연이 아니었어! 금융 공부를 철저히 한 덕분에 여기까지 이끌려 왔지\n 그래 이것이 진정한 \'성공한 인생\'이야 그리고 다음은 네 차례야!!';

    for (let i = 0; i < innerText.length; i++) {
      setTimeout(() => {
        if (innerText[i] === '\n') {
          this.inputText += '<br>';
        } else {
          this.inputText += innerText[i];
        }
      }, i * 50);
    }
  }
};
</script>
